enc = [0xF3, 0x2E, 0x18, 0x36, 0xE1, 0x4C, 0x22, 0xD1, 0xF9, 0x8C,
  0x40, 0x76, 0xF4, 0x0E, 0x00, 0x05, 0xA3, 0x90, 0x0E, 0xA5]

flag = []
flag.append(ord('#'))

# (x[-1]^18 * 3 +2) ^ (x[-1]%7 + x)
for i in range(len(enc)):
    temp = (enc[i]^((flag[i]^18)*3+2))&0xff
    temp = temp - (flag[i]%7)
    flag.append(temp)
print(flag)
print(''.join(chr(i) for i in flag))